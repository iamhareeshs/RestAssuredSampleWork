package apitest.expectedresponse.objects;

public class ExpectedMissDistance {
	private String astronomical;
	private String lunar;
	private String kilometers;
	private String miles;

	/**
	 * @return
	 */
	public String getAstronomical() {
		return astronomical;
	}

	/**
	 * @param value
	 */
	public void setAstronomical(String value) {
		this.astronomical = value;
	}

	/**
	 * @return
	 */
	public String getLunar() {
		return lunar;
	}

	/**
	 * @param value
	 */
	public void setLunar(String value) {
		this.lunar = value;
	}

	/**
	 * @return
	 */
	public String getKilometers() {
		return kilometers;
	}

	/**
	 * @param value
	 */
	public void setKilometers(String value) {
		this.kilometers = value;
	}

	/**
	 * @return
	 */
	public String getMiles() {
		return miles;
	}

	/**
	 * @param value
	 */
	public void setMiles(String value) {
		this.miles = value;
	}
}
