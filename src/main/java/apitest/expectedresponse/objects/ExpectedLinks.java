package apitest.expectedresponse.objects;

public class ExpectedLinks {
	private String next;
	private String prev;
	private String self;

	/**
	 * @return
	 */
	public String getNext() {
		return next;
	}

	/**
	 * @param value
	 */
	public void setNext(String value) {
		this.next = value;
	}

	/**
	 * @return
	 */
	public String getPrev() {
		return prev;
	}

	/**
	 * @param value
	 */
	public void setPrev(String value) {
		this.prev = value;
	}

	/**
	 * @return
	 */
	public String getSelf() {
		return self;
	}

	/**
	 * @param value
	 */
	public void setSelf(String value) {
		this.self = value;
	}
}
